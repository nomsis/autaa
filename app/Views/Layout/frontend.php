        <h1 align="center">Historie</h1>
            <p>K občanským sdružením a spolkům působícím v Hradčovicích patří Sportovní klub Hospodářské služby Sokol Hradčovice, ve zkratce SK HS Sokol Hradčovice. Jak vyplývá z uvedeného názvu, má tato sportovní organizace své historické kořeny v jednotě sokolské, která byla v Hradčovicích založena počátkem dvacátých let minulého dvacátého století.</p>
            <p>Tehdy, pod záštitou mateřské jednoty Sokol Uherský Brod a jako její pobočka, byla 12.srpna 1920 v Hradčovicích založena jednota sokolská, čítající při svém vzniku 61 členů. Stalo se tak poté, co již 20. února téhož roku byla založena sokolská organizace ve vedlejších Veletinách, aby pak, a to od roku 1931, se obě tyto jednoty osamostatnily a začaly působit společně pod názvem Sokol Hradčovice - Veletiny, čímž předznamenaly i pozdější poválečné obnovení činnosti organizace za společné účasti členů z Hradčovic a Veletin, jak je tomu v případě SK HS Sokol Hradčovice dodnes.</p>
                
        
